N, M, K = map(int, input().split())

# 앞면과 뒷면에 같은 모양으로 선택할 수 있는 카드의 최대 개수는 min(M, N - K)이다.
# 여기서 min(M, N - K)은 O와 X 중에서 뒷면에 적힐 문자를 선택한 개수를 나타낸다.
# 그 이유는 M개의 O 중에서 뒷면에 선택할 개수를 먼저 결정하고, 나머지 N - M개의 X 중에서 뒷면에 선택할 개수를 결정한다.
# 만약 M < N - K라면, O를 선택할 수 있는 카드의 개수가 더 적으므로 결과는 M이 되고,
# 그렇지 않으면, X를 선택할 수 있는 카드의 개수가 더 적으므로 결과는 N - K가 된다.
result = max(M, N - K)
print(result)


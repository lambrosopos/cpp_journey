# 2 1 5 => 4
# 3 1 5 => 2
# 5 1 6 => 2
# 100 99 1000000000 => 999999901 
up, down, height = map(int, input().split())

"""
2 1 5 => 4

(5 - 2) / (2 - 1) => 3 + 1
(5 - 3) / (3 - 1) => 1 + 1
(6 - 5) / (5 - 1) => 0.25 + 1
(1000000000 - 100) / (100 - 99) => 999999900 + 1
"""

ans = (height - up) / (up - down)

if ans != int(ans):
    ans += 1

print(int(ans + 1))
